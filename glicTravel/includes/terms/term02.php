	<p class="tit">방침 및 절차</p>
	<p class="txt">이 방침 및 절차는 유니시티와 유니시티의 성공사업을 함께 진행하는 사업동반자(Bios Life Frances Owner, 이하 FO라함)와 소비회원 (Consuming Distributor)의 관계 및 자신의 유니시티 사업을 어떻게 수행할 것인지의 방식을 규율하는 것이며, 디스트리뷰터 등록계약, 후원수당 지급기준을 명시하는 것입니다. 유니시티의 디스트리뷰터는 유니시티사업을 수행함에 있어 본 방침 및 절차를 숙지하고 이를 준수할 의무가 있습니다. 유니시티가 제시한 방침 및 절차를 준수하지 않고 왜곡하였을 경우 유니시티는 관련 디스트리뷰터와의 계약을 유니시티의 선택에 의하여 해지 또는 유예할 수 있으며, 해당 디스트리뷰터는 유니시티가 부여한 각종 권리를 행사할 수 있는 자격을 상실할 수 있습니다. 개정된 다음의 방침 및 절차는 기존의 방침 및 절차를 대신합니다.</p>
 			
	
	<p class="tit">1. 유니시티 디스트리뷰터 계약 (등록방침)</p>

	<p class="txt">본 계약은 신청인과 유니시티간의 완전한 합의를 구성하며, 여하한 종류의 어떠한 다른 약속, 제의, 진술, 합의 또는 양해도 서면으로 이루어져 유니시티의 수권된 임원이 서명하지 않는 한, 유니시티를 구속하지 않습니다. 디스트리뷰터는 디스트리뷰터와의 사업에 관한 모든 법률을 준수하고, 여하한 불법적이거나 비합법적인 상행위 또는 사업에 연루되어서는 안됩니다.</p>

 	<p class="subtit">가. 디스트리뷰터 </p>
	<p class="txt">유니시티의 신규 신청 계약서를 작성하고 유니시티가 이를 받아들인 경우 디스트리뷰터 자격을 가지게 되며, 디스트리뷰터는 가입시 본인의 의사에 따라 FO와 소비회원으로 구분될 수 있습니다. 유니시티의 보상플랜에 참여하기 위하여 귀하는 FO가 되어야 하며, 소비회원은 본인의 희망에 따라 후원활동 및 유니시티 보상플랜에 참여하지 아니합니다. 다만 구매실적은 등록된 상위 FO의 실적으로 포함되어 산정됩니다. </p>

   	<p class="subtit">나. FO (Bios Life Frances Owner)</p>
	<p class="txt">FO란 당해 FO의 완성된 FO 신규신청계약서를 유니시티가 접수•수리하였으며, 방문판매등에 관한 법률에 의거하여 다단계 판매원으로 유니시티에 등록된 개인을 의미합니다. FO가 되기 위한 제품구매 요건 등 재정적 부담은 없으며 FO로 등록하는 것과 관련하여 제품을 구매하는 것은 선택사항입니다. 등록된 FO만이 제품을 판매하거나 유니시티 프로그램에 참여할 수 있습니다.<br>
	FO와 유니시티와의 계약은 FO 신규신청계약서를 회사로부터 승인을 받은 시점부터 구속력을 갖게 되며, 당사가 계약서를 수령할 때 기입되지 않은 계약서는 완전히 기입될 때까지 효력이 없는 것으로 간주합니다.<br>
	신청인은 유니시티가 FO로 수락한 경우, 유니시티의 대리인 또는 직원 자격이 아니라 자신의 계좌로 사업을 수행하는 독립적인 사업 동반자로서 유니시티의 FO가 됩니다. 각 FO는 대한민국 법률이 요구하는 허가를 득할 책임이 있습니다. (사업자 등록을 취득하기를 권고합니다.)<br>
	FO는 모든 소득세 및 기타 세금의 납부를 포함하여 FO쉽의 전체적인 운영에 대한 책임이 있습니다.</p>

	<p class="subtit">다. 소비회원</p>
	<p class="txt">소비회원은 소비를 목적으로 유니시티와 직접 계약하여, 유니시티의 소비회원으로서 제품을 구입, 사용할 수 있는 권리를 가집니다. 소비회원은 본인의 의사에 따라 자가소비만을 목적으로 등록하는 디스트리뷰터로 유니시티의 보상플랜에 참여하지 않으며, 후원활동에 종사할 수 없습니다. 소비회원은 유니시티의 제품을 회원가격으로 구매할 수 있는 권리를 가지며, 당해 구매실적은 등록 상위 FO의 실적으로 포함되어 산정됩니다.<br>
	1) 등록: 소비회원등록신청서를 작성하여 유니시티에 접수시킴으로써 효력을 발합니다.<br>
	2) FO로의 전환방침: 소비회원으로 등록, 활동하다가 FO로의 전환을 원하는 소비회원은 자발적 해지 후 (해지통지서 접수) 즉시 FO로의 등록을 할 수 있습니다. 단, 다른 후원자로 변경하여 FO로의 등록을 원할 경우, 해지 후 6개월 경과시점에서 FO로 등록할 수 있습니다.</p>

	<p class="subtit">라. 가족방침</p>
	<p class="txt">부부 및 동거자녀들은 동일한 디스트리뷰터로 간주됩니다. 부부는 서로 동거자녀를 후원할 수 없으며, 서로 다른 후원자를 가질 수도 없습니다.<br>
	1) 등록하기를 희망하는 부부는 한 부의 신청서를 작성하여 각각 서명한 후 한 사람의 후원자를 선정하여 유니시티에 제출하여야 합니다.<br>
	2) 이미 기존에 등록하여 활동하고 있는 디스트리뷰터의 배우자가 등록하여 유니시티의 디스트리뷰터가 되기를 원할 경우 그 배우자는 상대 배우자와 동일한 디스트리뷰터쉽 내에서 사업을 해야 합니다. 유니시티는 부부가 별도의 디스트리뷰터로 등록하여 각각 다른 후원자를 가지고 사업하는 것을 금지합니다. 해당사항에 대하여 적발시 유니시티는 당해 디스트리뷰터의 자격을 유예시키거나 또는 해지시킬 수 있습니다.<br>
	3) 부부가 공동으로 등록하여 유니시티의 디스트리뷰터로 활동하던 중 별거 또는 이혼을 하게 될 경우, 두 사람은 합의한 내용을 유니시티에 제출합니다. 이 경우 유니시티는 관련된 사항의 수락여부를 조정 및 중재할 수 있습니다.<br>
	4) 동일한 거주지에 거주하는 부부 및 부모, 동거자녀도 동일한 디스트리뷰터쉽으로 간주되며, 별도의 회원쉽번호를 가질 수 없습니다. 단, 동일한 거주지에 거주하는 출가한 자녀는 가족등록방침의 적용을 받지 않습니다. </p>

	<p class="subtit">마. 최소연령</p>
	<p class="txt">디스트리뷰터는 만 19세 이상이어야 합니다. 단, 대학생의 경우 성인이라도 신청이 금지됩니다.</p>

	<p class="subtit">바. 하나의 디스트리뷰터쉽</p>
	<p class="txt">어떠한 경우에도 한 개인은 오직 하나의 디스트리뷰터쉽에 가입하는 것만을 허용합니다. 유니시티는 한 개인이 1개 이외의 디스트리뷰터 등록번호를 소유하고 있음을 인정하지 않습니다. 한 주거지상의 가족은 하나의 회원쉽만 소유할 수 있습니다. 만일 해당 위반사항이 적발될 경우 유니시티는 그 재량에 따라 다음과 같은 조치를 취하게 됩니다. <br>
	1)	유니시티는 당해 디스트리뷰터의 기존 디스트리뷰터 번호를 인정하고 차선 등록되어 있는 디스트리뷰터 번호는 해지 조치하는 방식으로 우선 등록되어 있는 디스트리뷰터쉽으로 연결됩니다. 해지된 차선 디스트리뷰터의 하위라인은 우선 등록되어 있는 디스트리뷰터쉽으로 연결됩니다.<br>
	2)	당해 디스트리뷰터의 우선 등록번호와 차선 등록번호 모두를 유니시티 방침 및 절차에 준하여 해지 조치합니다. 이 경우 차선등록번호의 직후원자는 유니시티 방침 및 절차에 준하여 해지 조치됩니다. 관련된 사항은 유니시티의 판단에 의하여 우선 조치한 후, 해당 디스트리뷰터에게 통보하는 것을 원칙으로 합니다. 상기 규정은 당해 디스트리뷰터 및 그의 배우자에게도 해당됩니다. 또한, 하나 이상의 디스트리뷰터 신청서에 서명하는 신청자는 유니시티의 디스트리뷰터로서의 등록이 거부될 수 있습니다. 단, 유니시티 보상프로그램상의 AFP는 예외입니다.</p>

	<p class="subtit">사. 디스트리뷰터 등록번호</p>
	<p class="txt">디스트리뷰터 등록번호는 디스트리뷰터가 유니시티에 등록하는 때에 부여됩니다. 단, 디스트리뷰터 등록번호가 부여된 신청자의 서류가 위조이거나, 본인의 의사 없이 타인에 의하여 작성 또는 서명되었을 경우나 서명되었다고 판단되는 경우, 또한 허위의 정보나 부정확한 정보가 제공되었다고 판단되는 경우 당해 디스트리뷰터의 등록번호는 유니시티의 판단에 의하여 즉시 해지될 수 있습니다. (개인이 아닌 법인은 유니시티의 디스트리뷰터가 될 수 없습니다.)</p>

	<p class="subtit">아. 디스트리뷰터쉽의 양도</p>
	<p class="txt">독립적인 유니시티 다단계 판매 사업은 판매에 의해 유상 양도, 무상양도 및 기타 여하한 방식에 의하여 이전될 수 없습니다.</p>

	<p class="subtit">자. 디스트리뷰터 갱신</p>
	<p class="txt">1) 디스트리뷰터의 계약은 계약 성립 후 6개월간 유효합니다. 자동 갱신 기준은 6개월간 유니시티의 판단하에 사업의사가 있다고 생각되거나, 뚜렷한 활동 증거가 있을 시 자동 갱신됩니다.<br>
	예> 뚜렷한 활동 증거 <br>
	① 제품주문 (판매보조물 포함)<br>
	② 보너스 수급<br>
	2) 유니시티가 판단하기에 해당 디스트리뷰터의 활동이 지난 12개월 동안 유니시티 및 유니시티의 디스트리뷰터 조직의 이익에 반하였거나, 본 방침 및 절차를 위반하였거나 또는 디스트리뷰터 활동이 유니시티가 만족할 만한 수준에 이르지 못하였다고 판단될 시에는 해당 디스트리뷰터의 갱신을 거부할 수 있는 권한이 유니시티에 주어집니다.</p>

	<p class="subtit">차. 판매원 목록</p>
	<p class="txt">유니시티 판매원의 목록, 활동, 보고, 계보 및 판매원에 대한 기타 정보 ‘( 본건 복록들’)는 오로지 판매원의 유니시티 사업을 촉진시키기 위해서만 사용되어야 하며, 다른 목적으로 본건 목록들의 부분 혹은 전체를 기존 판매원, 경쟁업체 및 일반인을 비롯한 제 3자에게 공개되어서는 안됩니다.</p>

	<p class="subtit">카. 교원 및 공무원 등록 금지</p>
	<p class="txt">2002년 7월 1일부터 확정•시행되는 개정된 ‘방문판매 등에 관한 법률’에 의거하여 공무원 및 교원의 경우 유니시티의 FO로의 등록이 금지됩니다.</p>

	<p class="subtit">타. 유니시티의 등록된 사업자는 유니시티에서 취급한 직급 또는 정보를 이용하여 유니시티 사업이 아닌 다른 목적으로 개인의 이익을 도모하는 것을 허용하지 않으며, 또한 당사의 판매 조직을 이용하여 타 디스트리뷰터에게 유니시티의 제품이 아닌 상품의 판매 또는 용역의 제공을 위탁하거나 알선하도록 하는 것을 금지합니다. 이러한 행위는 유니시티가 권장하고 보호하는 디스트리뷰터 그리고 사업방침을 위협하는 요소로 간주하여 이러한 사실이 적발되었을 경우 해당 디스트리뷰터에게 부여된 자격을 해지할 수 있으며, 이러한 사실이 회사 또는 타 디스트리뷰터의 사기를 저해하거나 손해를 입힐 경우 당사는 이에 대한 법적인 책임을 물을 수 있습니다.</p>


	<p class="tit">2 디스트리뷰터 활동(사업방침)</p>

	<p class="subtit">가. 후원</p>

	<p class="txt">1) 디스트리뷰터의 희망자에게 유니시티 프로그램을 제의할 때 유니시티 디스트리뷰터들은 생략, 왜곡 또는 허위의 설명 없이 프로그램 전체를 제시해야 합니다. 유니시티 프로그램과 관련하여 디스트리뷰터가 행하는 추가적 제의, 진술 또는 합의는 금지되며, 디스트리뷰터쉽의 해지를 초래할 수 있습니다. 때대로 1인 또는 그 이상의 개인들이 동일한 디스트리뷰터 희망자와 접촉할 가능성이 있으며, 이러한 경우 후원권과 관련하여 분쟁이 초래될 수 있습니다. 유니시티는 그러한 분쟁을 중재하지 않으며, 신청서에 서명이 기재된 사람을 후원자로 인정합니다. 둘 또는 그 이상의 신청서가 접수된 경우, 유니시티는 처음에 접수된 것을 인정합니다. 신규 디스트리뷰터는 자신이 후원자를 선택할 권리를 가지며 그에 대한 변경은 허용되지 않습니다. 어떠한 유니시티 디스트리뷰터도 후속 디스트리뷰터쉽에 대하여 소유지분, 운영 또는 관리통제권을 갖거나 또는 그로부터 직접 간접으로 이익을 취할 수 없습니다.<br>
	2) 후원자의 책임<br>
	① 후원자는 자신의 하위조직에 속한 디스트리뷰터들의 전체 계약약정과 해당 법률, 명령, 규정을 제대로 이해하고 이를 준수할 수 있도록 최상의 노력을 하여야 합니다. 단, 후원자들은 개인적으로 후원하는 디스트리뷰터들에게 교육이나 교육시설에 대한 비용을 징수할 수 없습니다. 또한 고객과 자신의 하위 조직간에 발행하는 분쟁을 중재하고, 우호적이고 신속하게 이를 해결하여야 합니다. 또한 후원자들은 본인이 자필서명한 어떤 문건에 대해서도 책임을 져야 합니다.<br>
	② 후원자는 하위조직 디스트리뷰터들의 성공적인 리더가 되기 위하여 후원인의 책임을 완수하여야 합니다. 후원자는 자신의 하위조직 구성원들을 지원하고, 정보를 제공하며 그들의 사업이 성공하도록 도울 뿐만 아니라 회사의 지침과 제품판매방법 및 교육들이 올바르게 진행되도록 성실하게 관리감독하고 지원하도록 노력하여야 합니다.<br>
	③ 회사는 이러한 내용이 올바르게 이루어질 때 후원자와 하위조직의 건전하고 원활한 조직문화가 구성된다고 믿으며 이러한 후원활동을 장려하며 보호합니다. 따라서 회사는 후원자의 책임에 따른 활동이 올바르게 이루어지지 않는다고 판단될 경우 다수의 디스트리뷰터를 보호하기 위하여 이에 관여하여 책임을 올바르게 수행하지 않는 후원자에게 시정을 요구할 수 있습니다.<br>
	④ 회사가 요구한 시정조치가 이루어지지 않을 경우 해당 디스트리뷰터가 후원자의 책임 및 권한을 포기하는것으로 간주하여 하위조직에 대한 권리는 당해 조직의 자격있는 차상위 후원자에게 연결될 수 있습니다. 또한 하위 조직을 후원하기 위하여 회사가 사전에 서면으로 요청한 신제품 소개, 보상플랜 설명, 관련법규 설명 등 이에 준하는 각종 후원활동을 특별한 사정없이 이행하지 아니한 경우 그 불이행의 정도에 비례하여 회사에 의하여 후원수당이 감액 될 수 있습니다.<br>
	3) 상호후원<br>
	디스트리뷰터는 다른 유니시티 디스트리뷰터가 여하한 다른 다단계판매회사에 들어가도록 후원할 수 없습니다. 아울러, 어떤 디스트리뷰터도 다른 디스트리뷰터가 어떤 사람을 통하여 다른 다단계 판매회사에 들어가도록 후원 받게 하는 행위에 참여할 수 없습니다. 후원을 행하여 2명의 디스트리뷰터들 사이에 귀중한 사업관계가 형성될 때에 중요한 투자가 이루어집니다. 유니시티는 이러한 관계를 보호할 책임이 있다고 믿으며, 상호후원을 해지사유로 간주합니다.</p>

	<p class="subtit">나. 후원자 변경</p>
	<p class="txt">1) 다단계 판매는 제품의 소매와 관계설정에 기초하여 이루어지는 사업입니다. 일단 어느 디스트리뷰터가 후원을 받게 되면 유니시티는 그러한 관계를 최대한으로 보호합니다. 따라서 사직을 하지 않고 후원자를 변경하는 것은 원칙적으로 허용되지 않습니다.<br>
	2) 사직한 후 6개월이 경과한 디스트리뷰터가 다시 사업을 하기 위해 등록을 할 경우에는 새로운 후원자로부터 후원을 받을 수 있습니다. 사직 후 6개월이 경과하지 않은 디스트리뷰터는 여하한 경우라도 후원자를 변경, 등록할 수 없습니다.<br>
	3) 유니시티와 디스트리뷰터 간의 관계가 여하한 사유에 의하여 해소된 경우, 유니시티는 당해 디스트리뷰터로부터 후원을 받는 디스트리뷰터들에 대하여 위와 같은 절차 없이 직권 변경할 수 있고, 위 2)항은적용되지 않습니다.<br>
	4) 유니시티는 디스트리뷰터가 다른 디스트리뷰터와 그 사람의 후원자 사이에서 어떤 식으로 간섭하는 행위든 인정하지 않습니다. 어떠한 디스트리뷰터든 다른 후원자나 당해 디스트리뷰터의 후원자의 라인을 변경하도록 직•간접적으로 제안, 장려, 유도 또는 기타 다른 방법으로 영향을 미치거나 설득하도록 시도해서는 안됩니다.</p>

	<p class="subtit">다. 디스트리뷰터에 대한 지급</p>
	<p class="txt">1) 일정한 이익<br>
	디스트리뷰터는 유니시티로부터 상품을 구매하여 소매고객에게 판매함으로써 소매이익을 얻을 수 있습니다. 디스트리뷰터는 그 하위 디스트리뷰터들에 대한 상품의 판매와 관련된 조직관리 및 교육훈련에 대하여 유니시티로부터 후원수당을 받을 수 있습니다.<br>
	2) 지급시기<br>
	지급시기는 월을 기준으로 합니다. 모든 주문은 월말 영업일의 근무시간 내에 접수된 주문만 인정합니다. 전월 영업에 대한 수당은 익월 20일에 지불됩니다.<br>
	3) 상계<br>
	디스트리뷰터가 유니시티가 판매 또는 제공하는 제품, 용역과 관련하여 유니시티에게 지불의 채무를 진 경우 유니시티는 유니시티가 그 디스트리뷰터에게 지불할 금액에서 동 채무에 해당하는 금액을 공제할 수 있습니다.</p>

	<p class="subtit">라. 제품</p>
	<p class="txt">1) 제품에 관한 주장<br>
	디스트리뷰터들은 제품의 내용물을 변경, 제거, 추가, 삭제하거나 제품의 포장을 다시 할 수 없으며, 라벨을 추가 또는 삭제, 변경하여 부착 할 수 없습니다. 유니시티는 국내 해당 법률(규정/규칙/조세 요건 및 기타 요건)에 따라 승인되고, 유니시티에 의하여 판매 또는 공급되는 제품 및 용역에 대하여 책임을 지며, 디스트리뷰터들은 유니시티가 사전에 승인하지 않는 한 유니시티 제품에 관한 구두 또는 서면의 주장을 할 수 없습니다. 제품에 대한 모든 광고와 설명은 유니시티가 배포한 자료에 기재되어 있는 내용과 동일해야 합니다. 또한 유니시티의 제품에 대하여 어떠한 의학적 주장도 허용되지 않습니다.<br>
	① 당사 제품에 대하여 어떠한 의학적 효과 및 효능을 주장하거나 특정한 병에 적합한 것으로 권유하거나 처방을 하는 행위 (어떠한 경우에도 건강기능식품은 질병을 치료하기 위한 처방된 의약품에 비유되어서는 안됩니다.)<br>
	② 보건복지부나 식품의약품안전청의 승인을 받았거나 받은 것처럼 암시하는 행위 (각각의 건강기능식품은 식품위생법과 건강기능식품법 등 해당 법률을 준수하여야 합니다.)<br>
	2) 디스트리뷰터는 소매고객에게 제품을 판매 시 소매영수증을 발급해주어야 하며, 모든 소매영수증은 2년간 보관해야 합니다. 또한 유니시티의 요청이 있는 경우 유니시티에게 제공하여야 합니다.<br>
	3) 부가가치세<br>
	유니시티 제품 및 판매보조자료는 부가가치세의 대상이 됩니다. 이러한 부가가치세는 유니시티가 징수하여 적절한 정부기관에 납부합니다.<br>
	4) 주문<br>
	주문량에 따른 수량을 인정받기 위해서는 모든 주문을 유니시티에 직접 하여야 합니다. 본인의 명의가 아닌 타인의 명의로 주문을 신청하는 경우 주문자와 주문을 대행하는 자 사이에서는 상호 승인이 요구됩니다. 사전 승인없이 임의로 디스트리뷰터가 주문을 대행할시에 발생하는 분쟁 및 문제에 대해서 유니시티는 이에 관여하지 않으며, 사전의 승인없이 주문을 대행한 디스트리뷰터가 이에 관련하여 해명해야 하는 책임을 집니다. 또한 다른 개인으로부터 특정한 승인을 받지 않고는 타인의 신용카드를 대행하여 가용할 수 없습니다.<br>
	5) 품질보증<br>
	유니시티 제품은 사용법에 따르는 경우 의도된 목적을 달성할 수 있는 품질과 적합성을 가지고 있음을 보증합니다. 본 보증 이외에 디스트리뷰터는 소비자 피해보상규정(경제기획고시 제 2003-18호) 제2조의 규정에 의한 권리를 포함하여 기타 법상 인정된 권리를 갖습니다. 유니시티는 모든 제품에 명시된 성분, 함량, 유통기한, 용법, 용량 및 기타 품질표시 내용을 보증합니다. 모든 디스트리뷰터들은 고객에게 제품의 용업, 용량, 보관, 취급 및 사용상의 주의사항을 충분히 교육하고, 판매 후 올바른 사용으로 최대의 효과를 볼 수 있도록 사후관리에 최선을 다하여야 합니다. 디스트리뷰터가 유니시티로부터 제품을 구매한 때에는 즉시 당해 상품에 하자 또는 부족이 있는지 여부를 확인하여야 합니다. 하자 또는 부족이 발견되면, 디스트리뷰터는 제품 수령일로부터 20일 이내에 그러한 하자 및 부족에 대하여 유니시티에 통지하고, 새로운 제품으로 교환하거나 상품 대금을 환불해 줄 것을 요구할 수 있습니다.<br>
	6) 수출방침<br>
	어떠한 유니시티 디스트리뷰터도 유니시티 제품을 수출하거나 그러한 제품을 한국에서 다른 국가로 수출하는 타인에게 판매할 수 없습니다.</p>

	<p class="subtit">마. 광고</p>
	<p class="txt">1) 매체로부터의 문의<br>
	정확하고 일관성 있는 회사 이미지를 유지하기 위하여 (라디오, 텔레비전 및 출판 발표를 포함한) 모든 매체로부터의 문의는 유니시티에 직접 위임하여야 합니다.<br>
	2) 광고 및 선전<br>
	유니시티는 디스트리뷰터들이 그들의 사업을 구축하기 위하여 실행 가능한 방법으로 광고 및 선전을 하는 것을 장려합니다. 단, 윤리적인 마케팅 노력을 유지하기 위해서는 엄격한 지침이 필요합니다. 따라서, 이러한 지침은 유니시티와 유니시티의 디스트리뷰터들간의 계약의 일부를 구성합니다. 각 디스트리뷰터는 FO 또는 소비회원이지 유니시티의 직원이 아니며, 유니시티의 직원이라고 주장할 수 없습니다. 아울러, 디스트리뷰터는 어떠한 방식으로도 당해 디스트리뷰터 또는 여하한 신규 디스트리뷰터가 유니시티에 의해 고용될 것이라고 진술하거나 그와 같이 암시할 수 없습니다. 또한 각 디스트리뷰터는 해당 법률, 명령 및 규정에 의거한 관련 법령상의 제한에 따라 제품의 소매활동과 독립 하위조직을 구축하는 독자적인 사업 기회에 관하여 판촉하거나 달리 설명하여야 합니다. 단, 다음에 명시된 사항에 대하여 광고 및 선전하는 것을 유니시티는 엄격히 금지합니다.<br>
	① 잠재적인 소득에 관하여 비합리적이고, 현혹적이거나 일반적이지 않은 선전을 하는 행위<br>
	② 특정인이 스스로 노력을 기울이지 않고도 유니시티의 디스트리뷰터나 유니시티와 연관하여 진술하는 본인이나 또는 타인에 대하여 어떠한 이득이나 수입을 얻을 수 있거나 얻을 것이라고 장담하거나 진술하는 행위<br>
