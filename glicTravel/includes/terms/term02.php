	-	유니시티는 당해 디스트리뷰터에게 “해지의사”를 밝히는 서면통지를 송부합니다.<br>
	-	위반한 디스트리뷰터는 30일간의 기간 동안 자신의 입장을 서면으로 제출하여 검토 받습니다. 그 기간 동안의 주문은 모두 보류됩니다.<br>
	-	유니시티는 해지여부에 관한 최종적인 결정을 합니다.<br>
	-	(자발적 또는 강제적인)해지의 경우, 당해 디스트리뷰터의 하위조직에 대한 수당 및 수석 디스트리뷰터의 장려금은 자격 있는 차상위 디스트리뷰터로부터 시작하여 지급됩니다.</p>
	
	<p class="subtit">가. 유니시티에 의한 해지</p>
	
	<p class="txt">1) 해지사유<br>
	① 디스트리뷰터 신청서에 허위사실을 기재하였을 경우<br>
	② 디스트리뷰터가 유니시티사업을 수행하는데 있어서 심각한 잘못을 저질렀다고 판단되는 경우<br>
	③ 방침 및 절차를 위반하고 그러한 위반행위를 유니시티가 서면 또는 구두로 시정을 요청한 바에 따라 15일 이내에 시정하지 않거나, 반복하여 방침 및 절차를 위반한 경우<br>
	④ 디스트리뷰터가 사망할 경우 단, 해당 디스트리뷰터 사망 후 15일 이내에 해당 디스트리뷰터의 법적 상속인이 상속절차를 거쳐 유니시티에 디스트리뷰터쉽의 상속을 요청하였을 경우는 제외됨.<br>
	⑤ 유니시티가 규정한 비윤리적인 행위를 범했다고 판단되는 경우<br>
	⑥ 유니시티가 정하는 일정기간 동안 활동을 하지 않은 경우<br>
	⑦ 회사 내에서 이미 등록하여 사업을 하고 있는 다른 디스트리뷰터를 본인의 하위 조직으로의 등록을 종용하거나 본인을 후원자로 등록을 하게한 행위<br>
	⑧ 이미 등록을 하여 사업활동을 하고 있는 디스트리뷰터가 가족 또는 타인의 명의를 이용하여 회사 내에서 후원자를 변경하여 등록을 하는 경우, 이 경우에는 후원자 변경 후에 발생한 	매출에 대해 지급된 수당을 회수하여 원래의 후원자에게 지급하고 후원자를 변경한 디스트리뷰터의 하위조직은 원래의 후원자에게 편입한다.<br>
	⑨ 방침 및 절차를 위반하여 회사로부터 디스트리뷰터쉽을 해지 또는 박탈당한 디스트리뷰터가 다시 사업을 하기 위해 등록을 원할때에는 회사가 해지 또는 박탈되었던 사유를 검토하여 	등록을 거절할 수 있다.</p>
	
	<p class="subtit">나. 디스트리뷰터에 의한 자발적 해지</p>
	
	<p class="txt">디스트리뷰터 자신이 디스트리뷰터 활동을 그만두고자 할 때는 본인이 유니시티에 접수한 디스트리뷰터 신청계약서를 뒷면의 해지통지서 서식을 작성하여 유니시티에 제출하여야 하며, 접수 	후 즉시 해지 처리됩니다. </p>
	<p class="subtit">다. 해지 결과</p>
	
	<p class="txt">유니시티에 의한 해지 또는 디스트리뷰터 자진 해지의 결과는 디스트리뷰터의 자격이 완전히 취소 및 해제되었음을 말하며, 이는 디스트리뷰터의 자격으로 인하여 발생되는 모든 권리의 	소멸을 의미합니다. 또한, 해당 디스트리뷰터가 개인적으로 후원한 디스트리뷰터의 새로운 후원자는 전적으로 유니시티의 결정에 의하여 선정됩니다. 해지가 적용될 경우에도 회사에 진 	채무나 유니시티 디스트리뷰터의 행동규칙 및 윤리강령이나 규정위반에 따라 부과된 채무는 존속됩니다.</p>
	
	
	<p class="tit">6. 일반규정</p>
	
	<p class="subtit">가. 방침 및 절차의 해석권</p>
	<p class="txt">유니시티의 방침 및 절차에 규정된 모든 사항에 대하여 적합/부적합의 최종적인 결정 및 해석권은 유니시티에게 주어집니다.</p>
	<p class="subtit">나. 변경</p>
	<p class="txt">유니시티는 네트워크 마케팅 회사로서 계속 활동하기 위하여 방침 및 절차, 수당지급 기준, 디스트리뷰터 수첩 및 가격 결정과 관련하여 필요하다고 인정되는 변경 또는 조정을 행할 	권리가 있습니다. 모든 변경은 서면으로 발표되는 즉시 유니시티와 유니시티 디스트리뷰터간의 계약의 일부로 포함됩니다.</p>
	<p class="subtit">다. 준거법</p>
	<p class="txt">본 계약은 대한민국 법률에 따라 규정됩니다.</p>
	<p class="subtit">라. 소비자 피해 보상 보험</p>
	<p class="txt">직접판매공제조합(이하‘공제조합’)은 방문판매 등에 관한 법률에 의하여 등록한 다단계판매업자인 공제계약자가 재화 또는 용역(이하‘재화 등’)을 판매한 후 공제계약자의 재화 등의 	공급의무 불이행, 다단계판매원 또는 소비자(이하‘수혜자’)의 청약의 철회 및 계약의 해제(이하‘청약철회 등’)로 인한 대금 환급 의무를 공제계약자가 이행하지 아니함으로써 수혜자가 	입은 손해에 대하여 공정거래위원회 고시에 의거 수혜자가 다단계판매원인 경우 법이 정하는 대금환급금액의 70%(1인에 대한 공제금의 한도는 공제사고 발생일을 기준으로 역산하여 매 	3월의 기간 동안 5백만원), 수혜자가 소비자의 경우 법이 정하는 대금환급금액의 90%(미성년자 또는 60세 이상인 자는 100%), (1인에 대한 공제) 금의 한도는 공제사고 발생일을 	기준으로 역산하여 매 14일의 기간 동안 2백만원)를 보상합니다. 다만 다단계판매원으로부터 재화 등을 구매한 소비자에 대하여는 공제의 양도가 있는 경우를 제외하고 공제조합이 	소비자에게 피해 보상의무를 부담하지 아니합니다. 그밖에 자세한 내용은 공정거래위원회가 인정하는 신문, 공제조합의 공시 사이트(www.macco.or.kr) 또는 대표전화 02-566-1202 	및 보상문의 02-3498-4952를 통하여 공제조합의 피해보상 약관을 반드시 확인하시기 바랍니다.
	상기 피해보상 약관을 확인하지 아니함으로써 수혜자가 입은 손해에 대하여는 공제조합이 책임을 부담하지 아니합니다. (소비자 피해보상보험 번호: 1008)</p>
	
	<p class="subtit">마. 분쟁의 해결</p>
	<p class="txt">본 계약과 관련하여 유니시티와 디스트리뷰터 간에 법적인 분쟁이 발생하는 경우 당해 디스트리뷰터의 주소지 관할법원이 당해 분쟁에 대한 전속관할권을 갖습니다. 그러나 유니시티 	계약항에 야기될 수 있는 분쟁을 신속하게 해결하기 위해서 유니시티는 조정 / 중재 정책을 수립하여 운영할 수 있습니다.</p>
	<p class="subtit">바. 통지</p>
	<p class="txt">본 방침 및 절차에 관련된 통보, 요구사항 및 기타 서류는 유니시티 코리아(유) 본사로 우송하며, 디스트리뷰터에게는 그의 최근 디스트리뷰터 신청서에 기재된 주소로 등기우편으로 	발송됩니다. 우편을 통한 전달의 경우 정상적인 상황에서 도달되었을 시점을 전달된 시점으로 간주합니다.</p>

